/**
 * Content-aware inpainting (PatchMatch).
 *
 * @param pixels   RGBA flat canvas composite, width × height × 4 bytes.
 * @param width    Canvas width in pixels.
 * @param height   Canvas height in pixels.
 * @param mask     Single-channel fill mask from activeScope().selection.mask,
 *                 width × height bytes (255 = fill region, 0 = source region).
 * @param sourceMask  Optional source-eligibility mask (1 = eligible, 0 = excluded).
 *                    Pass undefined for unconstrained (sample entire image).
 * @returns        RGBA output buffer, same dimensions as pixels.
 *                 Pixels outside the mask are unchanged copies of the input.
 */
export async function inpaintRegion(
  pixels: Uint8Array,
  width: number,
  height: number,
  mask: Uint8Array,
  sourceMask?: Uint8Array,
): Promise<Uint8Array> {
  const m = await getPixelOps();
  const PATCH_SIZE = 4; // → 9×9 patches
  const byteLen = pixels.byteLength; // width * height * 4

  const pixelsPtr = m._malloc(byteLen);
  const maskPtr = m._malloc(mask.byteLength);
  const outPtr = m._malloc(byteLen);
  let sourceMaskPtr = 0;
  try {
    m.HEAPU8.set(pixels, pixelsPtr);
    m.HEAPU8.set(mask, maskPtr);
    if (sourceMask !== undefined) {
      sourceMaskPtr = m._malloc(sourceMask.byteLength);
      m.HEAPU8.set(sourceMask, sourceMaskPtr);
    }
    m._pixelops_inpaint(
      pixelsPtr,
      width,
      height,
      maskPtr,
      PATCH_SIZE,
      sourceMaskPtr,
      outPtr,
    );
    // Re-read HEAPU8 in case WASM memory grew during the call
    return m.HEAPU8.slice(outPtr, outPtr + byteLen);
  } finally {
    m._free(pixelsPtr);
    m._free(maskPtr);
    if (sourceMaskPtr !== 0) m._free(sourceMaskPtr);
    m._free(outPtr);
  }
}

/**
 * GrabCut segmentation (Rother et al. 2004).
 *
 * @param pixels    RGBA image, width×height×4 bytes.
 * @param width     Image width.
 * @param height    Image height.
 * @param trimap    Per-pixel trimap: 0=BG, 128=unknown, 255=FG. width×height bytes.
 * @param iterations EM iterations (3 is typically sufficient).
 * @param k         GMM components per class (5 recommended).
 * @returns         Binary alpha mask (0 or 255), width×height bytes.
 */
export async function grabCut(
  pixels: Uint8Array,
  width: number,
  height: number,
  trimap: Uint8Array,
  iterations = 3,
  k = 5,
): Promise<Uint8Array> {
  const m = await getPixelOps();
  const n = width * height;

  const rgbaPtr = m._malloc(pixels.byteLength);
  const trimapPtr = m._malloc(n);
  const alphaPtr = m._malloc(n);
  try {
    m.HEAPU8.set(pixels, rgbaPtr);
    m.HEAPU8.set(trimap, trimapPtr);
    m._pixelops_grabcut(
      rgbaPtr,
      width,
      height,
      trimapPtr,
      alphaPtr,
      iterations,
      k,
    );
    // Re-read HEAPU8 in case WASM memory grew
    return m.HEAPU8.slice(alphaPtr, alphaPtr + n);
  } finally {
    m._free(rgbaPtr);
    m._free(trimapPtr);
    m._free(alphaPtr);
  }
}

