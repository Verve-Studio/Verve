/** Flood-fill starting at (x, y) with the given RGBA colour.
 *  tolerance: max squared Euclidean distance in RGBA space (0 = exact match). */
export async function floodFill(
  pixels: Uint8Array,
  width: number,
  height: number,
  x: number,
  y: number,
  r: number,
  g: number,
  b: number,
  a: number,
  tolerance = 0,
): Promise<Uint8Array> {
  const m = await getPixelOps();
  return withInPlaceBuffer(m, pixels, (ptr) =>
    m._pixelops_flood_fill(ptr, width, height, x, y, r, g, b, a, tolerance),
  );
}

/** Flood-fill for float32 RGBA layers (rgba32f).
 *  r/g/b/a are in [0,1] (may be >1 for HDR).
 *  tolerance: per-channel Euclidean threshold in [0,1] float space. */
export async function floodFillF32(
  pixels: Float32Array,
  width: number,
  height: number,
  x: number,
  y: number,
  r: number,
  g: number,
  b: number,
  a: number,
  tolerance = 0,
): Promise<Float32Array> {
  const m = await getPixelOps();
  const byteLen = pixels.byteLength;
  const ptr = m._malloc(byteLen);
  try {
    m.HEAPF32.set(pixels, ptr >> 2);
    m._pixelops_flood_fill_f32(ptr, width, height, x, y, r, g, b, a, tolerance);
    return new Float32Array(m.HEAPF32.buffer, ptr, pixels.length).slice();
  } finally {
    m._free(ptr);
  }
}

/** Generic 2-D convolution. kernelSize must be odd. src and dst are separate. */
export async function convolve(
  pixels: Uint8Array,
  width: number,
  height: number,
  kernel: Float32Array,
): Promise<Uint8Array> {
  const m = await getPixelOps();
  const kPtr = m._malloc(kernel.byteLength);
  try {
    m.HEAPF32.set(kernel, kPtr >> 2); // float32 view offset
    const kernelSize = Math.round(Math.sqrt(kernel.length));
    return withSrcDstBuffers(m, pixels, pixels.byteLength, (src, dst) =>
      m._pixelops_convolve(src, dst, width, height, kPtr, kernelSize),
    );
  } finally {
    m._free(kPtr);
  }
}

