/**
 * src/wasm/index.ts
 *
 * Async wrapper around the Emscripten pixelops WASM module.
 *
 * SETUP REQUIRED — before this module works you must compile the WASM:
 *   npm run build:wasm
 * This generates src/wasm/generated/pixelops.js and pixelops.wasm.
 *
 * Usage:
 *   import { getPixelOps, floodFill } from '@/wasm'
 *   const wasmReady = getPixelOps() // call once; subsequent calls return cache
 */

import type {
  PixelOpsModule,
  PixelOpsFactory,
  CurvesHistogramResult,
} from "./types";

// ─── Module singleton ─────────────────────────────────────────────────────────

let _module: PixelOpsModule | null = null;
let _initPromise: Promise<PixelOpsModule> | null = null;

/** Initialise and return the WASM module (idempotent, cached after first call). */
export async function getPixelOps(): Promise<PixelOpsModule> {
  if (_module) return _module;
  if (_initPromise) return _initPromise;

  _initPromise = (async () => {
    // Dynamic import so Vite only bundles these when getPixelOps() is called.
    // eslint-disable-next-line @typescript-eslint/ban-ts-comment
    // @ts-ignore — file is generated by `npm run build:wasm`
    const { default: factory } = (await import("./generated/pixelops.js")) as {
      default: PixelOpsFactory;
    };
    // eslint-disable-next-line @typescript-eslint/ban-ts-comment
    // @ts-ignore — generated asset URL resolved by Vite
    const wasmUrl = (await import("./generated/pixelops.wasm?url"))
      .default as string;

    _module = await factory({ locateFile: () => wasmUrl });
    return _module;
  })();

  return _initPromise;
}

// ─── Memory helpers ───────────────────────────────────────────────────────────

/**
 * Copies `data` into WASM memory, calls `fn(ptr)` which may modify it in-place,
 * then copies the (possibly modified) bytes back out and frees the WASM allocation.
 */
function withInPlaceBuffer(
  m: PixelOpsModule,
  data: Uint8Array,
  fn: (ptr: number) => void,
): Uint8Array {
  const ptr = m._malloc(data.byteLength);
  try {
    m.HEAPU8.set(data, ptr);
    fn(ptr);
    // Re-read HEAPU8 in case WASM memory grew during the call
    return m.HEAPU8.slice(ptr, ptr + data.byteLength);
  } finally {
    m._free(ptr);
  }
}

/**
 * Copies `data` into WASM memory, allocates a separate output buffer,
 * calls `fn(srcPtr, dstPtr)`, then returns the output bytes.
 */
function withSrcDstBuffers(
  m: PixelOpsModule,
  data: Uint8Array,
  dstByteLength: number,
  fn: (srcPtr: number, dstPtr: number) => void,
): Uint8Array {
  const srcPtr = m._malloc(data.byteLength);
  const dstPtr = m._malloc(dstByteLength);
  try {
    m.HEAPU8.set(data, srcPtr);
    fn(srcPtr, dstPtr);
    return m.HEAPU8.slice(dstPtr, dstPtr + dstByteLength);
  } finally {
    m._free(srcPtr);
    m._free(dstPtr);
  }
}

