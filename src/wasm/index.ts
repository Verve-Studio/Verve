/** Median-cut palette quantisation.
 *  Returns an RGBA Uint8Array of `maxColors * 4` bytes
 *  and the actual count of palette entries. */
export async function quantize(
  pixels: Uint8Array,
  maxColors: number,
): Promise<{ palette: Uint8Array; count: number }> {
  const m = await getPixelOps();
  const pixelCount = pixels.length / 4;
  const palBuf = new Uint8Array(maxColors * 4);
  const pixPtr = m._malloc(pixels.byteLength);
  const palPtr = m._malloc(palBuf.byteLength);
  try {
    m.HEAPU8.set(pixels, pixPtr);
    const count = m._pixelops_quantize(pixPtr, pixelCount, palPtr, maxColors);
    const palette = m.HEAPU8.slice(palPtr, palPtr + count * 4);
    return { palette, count };
  } finally {
    m._free(pixPtr);
    m._free(palPtr);
  }
}

/** Compute histogram for curves adjustment.
 *  Returns RGB, Red, Green, Blue channel histograms (256 bins each).
 *  Optional selectionMask weights the contribution of each pixel. */
export async function computeHistogramRGBA(
  pixelData: Uint8Array,
  width: number,
  height: number,
  mask?: Uint8Array,
): Promise<CurvesHistogramResult> {
  const m = await getPixelOps();
  const pixPtr = m._malloc(pixelData.byteLength);
  let maskPtr = 0;
  try {
    m.HEAPU8.set(pixelData, pixPtr);
    if (mask) {
      maskPtr = m._malloc(mask.byteLength);
      m.HEAPU8.set(mask, maskPtr);
    }

    // Call WASM function to get histogram buffer pointer.
    const histPtr = m._pixelops_curves_histogram(
      pixPtr,
      width,
      height,
      maskPtr,
    );
    if (!histPtr) {
      throw new Error("WASM histogram allocation failed");
    }

    // Re-read HEAPF32 in case memory grew.
    const heapf32 = m.HEAPF32;
    const histFloatOffset = histPtr >> 2; // Convert byte offset to float32 offset.
    const histogramFloat32 = heapf32.slice(
      histFloatOffset,
      histFloatOffset + 4 * 256,
    );

    // Free the histogram buffer returned by WASM.
    m._free(histPtr);

    // Split into separate channels (each 256 float32s)
    const rgb = histogramFloat32.subarray(0, 256);
    const red = histogramFloat32.subarray(256, 512);
    const green = histogramFloat32.subarray(512, 768);
    const blue = histogramFloat32.subarray(768, 1024);

    return { rgb, red, green, blue };
  } finally {
    m._free(pixPtr);
    if (maskPtr) {
      m._free(maskPtr);
    }
  }
}

/**
 * Affine transform via inverse-mapping.
 * invMatrix: [a, b, tx, c, d, ty] — maps output (x,y) to source (u,v):
 *   u = a*x + b*y + tx
 *   v = c*x + d*y + ty
 * interp: 0=nearest, 1=bilinear, 2=bicubic
 */
export async function applyAffineTransform(
  src: Uint8Array,
  srcW: number,
  srcH: number,
  dstW: number,
  dstH: number,
  invMatrix: Float32Array,
  interp: number,
): Promise<Uint8Array> {
  const m = await getPixelOps();
  const matPtr = m._malloc(invMatrix.byteLength);
  try {
    m.HEAPF32.set(invMatrix, matPtr >> 2);
    return withSrcDstBuffers(m, src, dstW * dstH * 4, (srcPtr, dstPtr) =>
      m._pixelops_affine_transform(
        srcPtr,
        srcW,
        srcH,
        dstPtr,
        dstW,
        dstH,
        matPtr,
        interp,
      ),
    );
  } finally {
    m._free(matPtr);
  }
}

