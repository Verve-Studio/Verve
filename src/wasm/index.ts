/** Decode an OpenEXR file. */
export async function decodeExr(
  bytes: Uint8Array,
): Promise<{ pixels: Float32Array; width: number; height: number }> {
  const m = await getPixelOps();
  const srcPtr = m._malloc(bytes.byteLength);
  try {
    m.HEAPU8.set(bytes, srcPtr);
    const resultPtr = m._loadExr(srcPtr, bytes.byteLength);
    if (resultPtr === 0) throw new Error("EXR decode failed");
    const view = new DataView(m.HEAPU8.buffer, m.HEAPU8.byteOffset);
    const width = view.getInt32(resultPtr, true);
    const height = view.getInt32(resultPtr + 4, true);
    const pxPtr = view.getInt32(resultPtr + 8, true);
    const count = width * height * 4;
    const pixels = new Float32Array(count);
    const heap = new Float32Array(m.HEAPU8.buffer, m.HEAPU8.byteOffset);
    pixels.set(heap.subarray(pxPtr >> 2, (pxPtr >> 2) + count));
    m._freeExrResult(resultPtr);
    return { pixels, width, height };
  } finally {
    m._free(srcPtr);
  }
}

/** Encode RGBA float32 pixels to OpenEXR. compression: 0=none,1=zip,2=zips,3=piz; halfFloat: 0 or 1. */
export async function encodeExr(
  pixels: Float32Array,
  width: number,
  height: number,
  compression = 0,
  halfFloat = 0,
): Promise<Uint8Array> {
  const m = await getPixelOps();
  const srcPtr = m._malloc(pixels.byteLength);
  try {
    new Float32Array(
      m.HEAPU8.buffer,
      m.HEAPU8.byteOffset + srcPtr,
      pixels.length,
    ).set(pixels);
    const resultPtr = m._saveExr(srcPtr, width, height, compression, halfFloat);
    if (resultPtr === 0) throw new Error("EXR encode failed");
    const view = new DataView(m.HEAPU8.buffer, m.HEAPU8.byteOffset);
    const bytesPtr = view.getInt32(resultPtr, true);
    const outLen = view.getInt32(resultPtr + 4, true);
    const out = new Uint8Array(outLen);
    out.set(m.HEAPU8.subarray(bytesPtr, bytesPtr + outLen));
    m._freeExrBytes(resultPtr);
    return out;
  } finally {
    m._free(srcPtr);
  }
}

export interface ExrDecodedLayer {
  name: string;
  width: number;
  height: number;
  offsetX: number;
  offsetY: number;
  pixels: Float32Array;
}

export interface ExrMultiDecodeResult {
  canvasWidth: number;
  canvasHeight: number;
  layers: ExrDecodedLayer[];
}

/** Read a null-terminated UTF-8 string from the WASM heap. */
function readCString(heap: Uint8Array, ptr: number): string {
  let end = ptr;
  while (end < heap.length && heap[end] !== 0) end++;
  return new TextDecoder("utf-8").decode(heap.subarray(ptr, end));
}

/** Decode an EXR file into one or more named layers. */
export async function decodeExrLayers(
  bytes: Uint8Array,
): Promise<ExrMultiDecodeResult> {
  const m = await getPixelOps();
  const srcPtr = m._malloc(bytes.byteLength);
  let resultPtr = 0;
  try {
    m.HEAPU8.set(bytes, srcPtr);
    resultPtr = m._loadExrLayers(srcPtr, bytes.byteLength);
    if (resultPtr === 0) throw new Error("EXR multi-layer decode failed");

    const view = new DataView(m.HEAPU8.buffer, m.HEAPU8.byteOffset);
    const canvasWidth = view.getInt32(resultPtr, true);
    const canvasHeight = view.getInt32(resultPtr + 4, true);
    const numLayers = view.getInt32(resultPtr + 8, true);
    const layersPtr = view.getInt32(resultPtr + 12, true);

    const layers: ExrDecodedLayer[] = [];
    const STRIDE = 24; // ExrLayerOut size
    for (let i = 0; i < numLayers; i++) {
      const base = layersPtr + i * STRIDE;
      const w = view.getInt32(base, true);
      const h = view.getInt32(base + 4, true);
      const offX = view.getInt32(base + 8, true);
      const offY = view.getInt32(base + 12, true);
      const namePtr = view.getInt32(base + 16, true);
      const pxPtr = view.getInt32(base + 20, true);
      const name = readCString(m.HEAPU8, namePtr);
      const count = w * h * 4;
      const pixels = new Float32Array(count);
      const heapF32 = new Float32Array(m.HEAPU8.buffer, m.HEAPU8.byteOffset);
      pixels.set(heapF32.subarray(pxPtr >> 2, (pxPtr >> 2) + count));
      layers.push({
        name,
        width: w,
        height: h,
        offsetX: offX,
        offsetY: offY,
        pixels,
      });
    }
    return { canvasWidth, canvasHeight, layers };
  } finally {
    if (resultPtr !== 0) m._freeExrLayersResult(resultPtr);
    m._free(srcPtr);
  }
}

/**
 * Encode multiple full-canvas RGBA float32 layers into a single-part EXR with
 * channel-named groups ("<LayerName>.R/G/B/A").  Each layer's `pixels` must be
 * width*height*4 floats.
 */
export async function encodeExrLayers(
  layers: ReadonlyArray<{ name: string; pixels: Float32Array }>,
  width: number,
  height: number,
  compression = 0,
  halfFloat = 0,
): Promise<Uint8Array> {
  if (layers.length === 0) throw new Error("encodeExrLayers: no layers given");
  const m = await getPixelOps();

  // Pack names: null-separated UTF-8.
  const enc = new TextEncoder();
  const nameBufs = layers.map((l) => enc.encode(l.name));
  const nameBytes = nameBufs.reduce((s, b) => s + b.length + 1, 0);
  const namesBuf = new Uint8Array(nameBytes);
  {
    let off = 0;
    for (const nb of nameBufs) {
      namesBuf.set(nb, off);
      off += nb.length;
      namesBuf[off++] = 0;
    }
  }

  // Pack pixels: layer-major contiguous.
  const pixelsPerLayer = width * height * 4;
  const totalFloats = pixelsPerLayer * layers.length;
  const namesPtr = m._malloc(namesBuf.byteLength);
  const pixelsPtr = m._malloc(totalFloats * 4);
  let resultPtr = 0;
  try {
    m.HEAPU8.set(namesBuf, namesPtr);
    const heapF32 = new Float32Array(
      m.HEAPU8.buffer,
      m.HEAPU8.byteOffset + pixelsPtr,
      totalFloats,
    );
    for (let i = 0; i < layers.length; i++) {
      if (layers[i].pixels.length !== pixelsPerLayer) {
        throw new Error(
          `encodeExrLayers: layer ${i} (${layers[i].name}) has ${layers[i].pixels.length} floats, expected ${pixelsPerLayer}`,
        );
      }
      heapF32.set(layers[i].pixels, i * pixelsPerLayer);
    }
    resultPtr = m._saveExrLayers(
      width,
      height,
      layers.length,
      namesPtr,
      pixelsPtr,
      compression,
      halfFloat,
    );
    if (resultPtr === 0) throw new Error("EXR multi-layer encode failed");
    const view = new DataView(m.HEAPU8.buffer, m.HEAPU8.byteOffset);
    const bytesPtr = view.getInt32(resultPtr, true);
    const outLen = view.getInt32(resultPtr + 4, true);
    const out = new Uint8Array(outLen);
    out.set(m.HEAPU8.subarray(bytesPtr, bytesPtr + outLen));
    return out;
  } finally {
    if (resultPtr !== 0) m._freeExrBytes(resultPtr);
    m._free(namesPtr);
    m._free(pixelsPtr);
  }
}

// ─── DDS I/O ─────────────────────────────────────────────────────────────────

export const DdsFormat = {
  BC1: 1,
  BC2: 2,
  BC3: 3,
  BC4: 4,
  BC5: 5,
  BC6H: 6,
  BC7: 7,
  RGBA8: 8,
  RGBA32F: 9,
} as const;
export type DdsFormat = (typeof DdsFormat)[keyof typeof DdsFormat];

export const DdsHeaderMode = {
  AUTO: 0,
  DX9: 1,
  DX10: 2,
} as const;

export interface DdsInfo {
  width: number;
  height: number;
  fmt: DdsFormat;
  mipLevels: number;
}

function ddsErrorFromCode(code: number): string {
  switch (code) {
    case -1:
      return "DDS: invalid file";
    case -2:
      return "DDS: unsupported format";
    case -3:
      return "DDS: output buffer too small";
    default:
      return `DDS: error code ${code}`;
  }
}

/** Parse DDS header metadata. */
export async function getDdsInfo(bytes: Uint8Array): Promise<DdsInfo> {
  const m = await getPixelOps();
  const dataPtr = m._malloc(bytes.byteLength);
  const outPtr = m._malloc(16); // 4 × int32
  try {
    m.HEAPU8.set(bytes, dataPtr);
    const err = m._pixelops_dds_get_info(dataPtr, bytes.byteLength, outPtr);
    if (err !== 0) throw new Error(ddsErrorFromCode(err));
    const view = new DataView(m.HEAPU8.buffer, m.HEAPU8.byteOffset);
    return {
      width: view.getInt32(outPtr, true),
      height: view.getInt32(outPtr + 4, true),
      fmt: view.getInt32(outPtr + 8, true) as DdsFormat,
      mipLevels: view.getInt32(outPtr + 12, true),
    };
  } finally {
    m._free(dataPtr);
    m._free(outPtr);
  }
}

/** Decode a DDS file to RGBA8 pixels. BC6H and RGBA32F are tonemapped to SDR. */
export async function decodeDds(
  bytes: Uint8Array,
): Promise<{ pixels: Uint8Array; width: number; height: number }> {
  const m = await getPixelOps();
  const info = await getDdsInfo(bytes);
  const { width, height } = info;
  const outSize = width * height * 4;
  const dataPtr = m._malloc(bytes.byteLength);
  const outPtr = m._malloc(outSize);
  try {
    m.HEAPU8.set(bytes, dataPtr);
    const err = m._pixelops_dds_decode(
      dataPtr,
      bytes.byteLength,
      outPtr,
      outSize,
    );
    if (err !== 0) throw new Error(ddsErrorFromCode(err));
    const pixels = new Uint8Array(outSize);
    pixels.set(m.HEAPU8.subarray(outPtr, outPtr + outSize));
    return { pixels, width, height };
  } finally {
    m._free(dataPtr);
    m._free(outPtr);
  }
}

/** Decode a DDS file to RGBA float32 pixels. */
export async function decodeDdsF32(
  bytes: Uint8Array,
): Promise<{ pixels: Float32Array; width: number; height: number }> {
  const m = await getPixelOps();
  const info = await getDdsInfo(bytes);
  const { width, height } = info;
  const outSize = width * height * 16; // 4 floats × 4 bytes
  const dataPtr = m._malloc(bytes.byteLength);
  const outPtr = m._malloc(outSize);
  try {
    m.HEAPU8.set(bytes, dataPtr);
    const err = m._pixelops_dds_decode_f32(
      dataPtr,
      bytes.byteLength,
      outPtr,
      outSize,
    );
    if (err !== 0) throw new Error(ddsErrorFromCode(err));
    const pixels = new Float32Array(width * height * 4);
    const heap = new Float32Array(m.HEAPU8.buffer, m.HEAPU8.byteOffset);
    pixels.set(heap.subarray(outPtr >> 2, (outPtr >> 2) + pixels.length));
    return { pixels, width, height };
  } finally {
    m._free(dataPtr);
    m._free(outPtr);
  }
}

/** Encode RGBA8 pixels to a DDS file (BC1, BC3, BC7). */
export async function encodeDds(
  pixels: Uint8Array,
  width: number,
  height: number,
  fmt: number,
  mipLevels: number = 1,
  headerMode: number = DdsHeaderMode.AUTO,
): Promise<Uint8Array> {
  const m = await getPixelOps();
  const outSize = m._pixelops_dds_get_encoded_size(
    width,
    height,
    fmt,
    mipLevels,
    headerMode,
  );
  if (outSize <= 0) throw new Error(ddsErrorFromCode(outSize));
  const srcPtr = m._malloc(pixels.byteLength);
  const outPtr = m._malloc(outSize);
  try {
    m.HEAPU8.set(pixels, srcPtr);
    const err = m._pixelops_dds_encode(
      srcPtr,
      width,
      height,
      fmt,
      mipLevels,
      headerMode,
      outPtr,
      outSize,
    );
    if (err !== 0) throw new Error(ddsErrorFromCode(err));
    const out = new Uint8Array(outSize);
    out.set(m.HEAPU8.subarray(outPtr, outPtr + outSize));
    return out;
  } finally {
    m._free(srcPtr);
    m._free(outPtr);
  }
}

